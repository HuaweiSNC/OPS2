def isdk_process_rpcreply(srpcreplystring,sXpath):
    
    lxpath = sXpath.split('/')
    scurelmnt = lxpath[len(lxpath)-1]
    lxpath.pop(0)
    sXpath = '/'.join(lxpath)
    
    sRpcReply = str(srpcreplystring)
    tdoc = etree.fromstring(sRpcReply)
    stag = ''.join(['{http://www.huawei.com/netconf/vrp}',scurelmnt])

    tdocContents = tdoc.getchildren()
    sOutXml = ''
    for elt in tdocContents:
        if 'data' == elt.tag:
            lChild = elt.getchildren()
            if len(lChild) > 0:
                sOutXml = etree.tostring(lChild[0], pretty_print=True, encoding=None)
                sOutXml = sOutXml.strip()
               
    tappdoc = etree.fromstring(sOutXml)

    lNonLeaf= []
    lLeaf =[]
    if str(tappdoc.tag) == stag :
        for elt in tappdoc.iterchildren():
            lhaschildren = []
            lhaschildren = elt.getchildren()
            if [] != lhaschildren :
                lNonLeaf.append(elt.tag[elt.tag.find('}') + 1:])
            else:
                lLeaf.append(elt.tag[elt.tag.find('}') + 1:])
    else:
        for elt in tappdoc.iterdescendants(tag=None):
            if str(elt.tag) == stag :
                for subElt in elt.iterchildren():
                    lhaschildren = []
                    lhaschildren = subElt.getchildren()
                    if [] != lhaschildren :
                        lNonLeaf.append(subElt.tag[subElt.tag.find('}') + 1:])
                    else:
                        lLeaf.append(subElt.tag[subElt.tag.find('}') + 1:])
    lNonLeaf = list(set(lNonLeaf))
    lLeaf = list(set(lLeaf))
    lLeafXpath =[]
    lNonLeafXpath =[]
    
    for item in lNonLeaf:
        lNonLeafXpath.append('/'.join([sXpath,item]))
    
    for item in lLeaf:
        lLeafXpath.append('/'.join([sXpath,item]))
    
    sleafOut = ''
    snonleafOut = ''
    if lLeafXpath != [] and lNonLeafXpath != []:
        sleafOut = '\n'.join(lLeafXpath)
        snonleafOut = '\n'.join(lNonLeafXpath)
    elif lLeafXpath != []:
        sleafOut = '\n'.join(lLeafXpath)
        snonleafOut = 'None'
    elif lNonLeafXpath != []:
        snonleafOut = '\n'.join(lNonLeafXpath)
        sleafOut = 'None'

    else:
        snonleafOut = 'None'
        sleafOut = 'None'
    
    snonleafOut = '<Non-Leaf> \n'+snonleafOut+' \n</Non-Leaf>'
    sleafOut = '<Leaf> \n'+sleafOut+' \n</Leaf>'
    
    sOutPut = '\n'.join([snonleafOut,sleafOut])
    return '<Learn> \n'+sOutPut+'\n</Learn>'
